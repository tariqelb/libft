#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dest;
	int		len;

	dest = NULL;
	len = ft_strlen(s1);
	dest = (char *) malloc(sizeof(char) * len + 1);
	if (dest == NULL)
		return (NULL);
	len = 0;
	while (s1[len] != '\0')
	{
		dest[len] = s1[len];
		len++;
	}
	dest[len] = '\0';
	return (dest);
}
